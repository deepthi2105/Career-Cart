    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String sql ="select * from payment where type=? and card_number = ? and cvv=? and expdate=?";
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/regis","root","15*Deeps");
            
            PreparedStatement pstmt = conn.prepareStatement(sql);
            
            String type1="";
            
            if(jRadioButton1.isSelected())
            {
                type1=jRadioButton1.getText();
            }
            if(jRadioButton2.isSelected())
            {
                type1=jRadioButton2.getText();
            }
            pstmt.setString(1, type1);
            pstmt.setString(2, jTextField1.getText());
            pstmt.setString(3, jTextField3.getText());
            pstmt.setString(4, jTextField4.getText());
             
            ResultSet rs= pstmt.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Payment Successful");
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid Card Number or CVV", "Unsuccessful transaction",JOptionPane.ERROR_MESSAGE);
            }
            conn.close();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

